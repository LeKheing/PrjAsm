package model;

import java.util.Date;

/**
 *
 * @author admin
 */
public class Order {

    private int id;
    private int uId;
    private int pId;
    private int quantity;

    public Order() {
    }

    public Order(int id, int uId, int pId, int quantity) {
        this.id = id;
        this.uId = uId;
        this.pId = pId;
        this.quantity = quantity;
    }

    // Getter and Setter methods
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUId() {
        return uId;
    }

    public void setUId(int uId) {
        this.uId = uId;
    }

    public int getPId() {
        return pId;
    }

    public void setPId(int pId) {
        this.pId = pId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
