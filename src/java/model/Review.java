package model;

/**
 *
 * @author admin
 */
public class Review {
    private int id;
    private int u_id;
    private int p_id;
    private String review_text;
    private String u_name;

    public Review(int id, int u_id, int p_id, String review_text, String u_name) {
        this.id = id;
        this.u_id = u_id;
        this.p_id = p_id;
        this.u_name = u_name;
        this.review_text = review_text;
    }

    // Getters and setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getU_id() {
        return u_id;
    }

    public void setU_id(int u_id) {
        this.u_id = u_id;
    }

    public int getP_id() {
        return p_id;
    }

    public void setP_id(int p_id) {
        this.p_id = p_id;
    }

    public String getReview_text() {
        return review_text;
    }

    public void setReview_text(String review_text) {
        this.review_text = review_text;
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }
    
    
}
